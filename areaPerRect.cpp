// This program calculates the area and perimeter of a rectangle with 9cm x 4cm dimensions

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions:\n";
    std::cout << "9cm x 4cm\n";
    std::cout << "\n";
    std::cout << "Area = " << 9 * 4 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (9 + 4) << " cm\n";
}
